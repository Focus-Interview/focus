  },

  data() {
    return {
      OV: undefined,
      session: undefined,
      mainStreamManager: undefined,
      publisher: undefined,
      subscribers: [],

      mySessionId: 'Room' + Math.floor(Math.random() * 100),
      myUserName: '지원자' + Math.floor(Math.random() * 100),

      // <------------------- 비디오/보이스 On/Off Start ---------->
      mute: false,
      closecamera: false,
      // <------------------- 비디오/보이스 On/Off End-- ---------->

      // <------------------- 화면 공유 Start -------------------->
      screenOV: undefined,
      screenSession: undefined,
      screenShareName: undefined,
      // <------------------- 화면 공유 End ---------------------->

      // <------------------- 채팅 기능 Start -------------------->
      chatLog: [],
      chatmodal: false,
      // <------------------- 채팅 기능 End ---------------------->

      active: false, // tool bar

      // <------------------ 참가자 목록 Start ------------------->
      participantsmodal: false,
      // <------------------- 참가자 목록 End -------------------->

      // <---------------- 발언자 하이라이팅 Start ---------------->
      isSpeakList: [],
      isSpeak: false,
      // <----------------- 발언자 하이라이팅 End ----------------->
    };
  },

  methods: {
    joinSession() {
      // --- Get an OpenVidu object ---
      this.OV = new OpenVidu();
      this.OV.setAdvancedConfiguration({
        publisherSpeakingEventOptions: {
          interval: 100,
          threshold: -50,
        },
      });

      // --- Init a session ---
      this.session = this.OV.initSession();

      // --- Specify the actions when events take place in the session ---

      // On every new Stream received...
      this.session.on('streamCreated', ({ stream }) => {
        const subscriber = this.session.subscribe(stream);
        this.subscribers.push(subscriber);
      });

      // On every Stream destroyed...
      this.session.on('streamDestroyed', ({ stream }) => {
        const index = this.subscribers.indexOf(stream.streamManager, 0);
        if (index >= 0) {
          this.subscribers.splice(index, 1);
        }
      });

      // On every asynchronous exception...
      this.session.on('exception', ({ exception }) => {
        console.warn(exception);
      });

      // <------------------- 채팅 기능 Start -------------------->
      this.session.on('signal', event => {
        this.chatLog.push([event.data, JSON.parse(event.from.data)]);
      });
      // <------------------- 채팅 기능 End ---------------------->

      // this.session.on('publisherStartSpeaking', (event) => {
      //   console.log('User' + event.connection.connectionId + ' start speaking');
      // });

      // this.session.on('publisherStopSpeaking', (event) => {
      //   console.log('User' + event.connection.connectionId + ' stop speaking');
      // });

      // Speech Start Detection
      this.session.on('publisherStartSpeaking', event => {
        this.isSpeakList.push(event.connection.connectionId);
        this.isSpeak = !this.isSpeak;
      });

      // Speech Stop Detection
      this.session.on('publisherStopSpeaking', event => {
        let temp = this.isSpeakList;
        let index = temp.indexOf(event.connection.connectionId, 0);
        if (index >= 0) {
          temp.splice(index, 1);
          this.isSpeakList = temp;
        }
        this.isSpeak = !this.isSpeak;
      });

      // --- Connect to the session with a valid user token ---

      // 'getToken' method is simulating what your server-side should do.
      // 'token' parameter should be retrieved and returned by your own backend
      this.getToken(this.mySessionId).then(token => {
        this.session
          .connect(token, { clientData: this.myUserName })
          .then(() => {
            // --- Get your own camera stream with the desired properties ---

            let publisher = this.OV.initPublisher(undefined, {
              audioSource: undefined, // The source of audio. If undefined default microphone
              videoSource: undefined, // The source of video. If undefined default webcam
              publishAudio: false, // Whether you want to start publishing with your audio unmuted or not
              publishVideo: true, // Whether you want to start publishing with your video enabled or not
              resolution: '640x720', // The resolution of your video
              frameRate: 30, // The frame rate of your video
              insertMode: 'APPEND', // How the video is inserted in the target element 'video-container'
              mirror: false, // Whether to mirror your local video or not
            });

            this.mainStreamManager = publisher;
            this.publisher = publisher;

            // --- Publish your stream ---

            this.session.publish(this.publisher);
          })
          .catch(error => {
            console.log(
              'There was an error connecting to the session:',
              error.code,
              error.message,
            );
          });
      });

      window.addEventListener('beforeunload', this.leaveSession);
    },

    leaveSession() {
      // --- Leave the session by calling 'disconnect' method over the Session object ---
      if (this.session) this.session.disconnect();

      this.session = undefined;
      this.mainStreamManager = undefined;
      this.publisher = undefined;
      this.subscribers = [];
      this.OV = undefined;
      (this.chatLog = []),
        window.removeEventListener('beforeunload', this.leaveSession);
    },

    updateMainVideoStreamManager(stream) {
      if (this.mainStreamManager === stream) return;
      this.mainStreamManager = stream;
    },

    getToken(mySessionId) {
      return this.createSession(mySessionId).then(sessionId =>
        this.createToken(sessionId),
      );
    },

    // See https://docs.openvidu.io/en/stable/reference-docs/REST-API/#post-openviduapisessions
    createSession(sessionId) {
      return new Promise((resolve, reject) => {
        axios
          .post(
            `${OPENVIDU_SERVER_URL}/openvidu/api/sessions`,
            JSON.stringify({
              customSessionId: sessionId,
            }),
            {
              auth: {
                username: 'OPENVIDUAPP',
                password: OPENVIDU_SERVER_SECRET,
              },
            },
          )
          .then(response => response.data)
          .then(data => resolve(data.id))
          .catch(error => {
            if (error.response.status === 409) {
              resolve(sessionId);
            } else {
              console.warn(
                `No connection to OpenVidu Server. This may be a certificate error at ${OPENVIDU_SERVER_URL}`,
              );
              if (
                window.confirm(
                  `No connection to OpenVidu Server. This may be a certificate error at ${OPENVIDU_SERVER_URL}\n\nClick OK to navigate and accept it. If no certificate warning is shown, then check that your OpenVidu Server is up and running at "${OPENVIDU_SERVER_URL}"`,
                )
              ) {
                location.assign(`${OPENVIDU_SERVER_URL}/accept-certificate`);
              }
              reject(error.response);
            }
          });
      });
    },

    // See https://docs.openvidu.io/en/stable/reference-docs/REST-API/#post-openviduapisessionsltsession_idgtconnection
    createToken(sessionId) {
      return new Promise((resolve, reject) => {
        axios
          .post(
            `${OPENVIDU_SERVER_URL}/openvidu/api/sessions/${sessionId}/connection`,
            {},
            {
              auth: {
                username: 'OPENVIDUAPP',
                password: OPENVIDU_SERVER_SECRET,
              },
            },
          )
          .then(response => response.data)
          .then(data => resolve(data.token))
          .catch(error => reject(error.response));
      });
    },

    // <------------------- 채팅 기능 Start -------------------->
    sendMessage(msg) {
      this.session
        .signal({ data: msg, to: [] })
        .then(() => {
          console.log('Message successfully sent');
        })
        .catch(error => {
          console.error(error);
        });
    },

    // <------------------- 채팅 기능 End ---------------------->

    // <------------------- 화면 공유 Start -------------------->
    startScreenShare() {
      this.screenOV = new OpenVidu();
      this.screenSession = this.screenOV.initSession();
      (this.screenShareName = this.myUserName + "'s Screen"),
        this.getToken(this.mySessionId)
          .then(token => {
            this.screenSession
              .connect(token, { clientData: this.screenShareName })
              .then(() => {
                let publisher = this.screenOV.initPublisher('html-element-id', {
                  videoSource: 'screen',
                  publishAudio: false,
                });

                try {
                  publisher.once('accessAllowed', () => {
                    let test = publisher.stream
                      .getMediaStream()
                      .getVideoTracks(); //eslint-disable-line no-unused-vars
                    publisher.stream
                      .getMediaStream()
                      .getVideoTracks()[0]
                      .addEventListener('ended', () => {
                        this.stopScreenShare();
                      });
                    this.screenSession.publish(publisher);
                  });

                  publisher.once('accessDenied', () => {
                    this.stopScreenShare();
                  });
                } catch (error) {
                  console.log(error);
                }
              });
          })
          .catch(() => {
            this.screenOV = undefined;
            this.screenSession = undefined;
          });
    },

    setDevice(deviceSettings) {
      this.videoId = deviceSettings.videoId;
      this.audioId = deviceSettings.audioId;
      this.videoOn = deviceSettings.videoOn;
      this.audioOn = deviceSettings.audioOn;
    },

    stopScreenShare() {
      this.screenSession.disconnect();
      this.screenOV = undefined;
      this.screenSession = undefined;
    },
    // <------------------- 화면 공유 End ---------------------->

    // <------------------- 비디오/보이스 On/Off Start ------------------->
    hoverToolbar() {
      this.active = true;
    },

    leaveToolbar() {
      this.active = false;
    },

    muteMySound() {
      this.publisher.publishAudio(false);
      this.mute = !this.mute;
    },

    unmuteMySound() {
      this.publisher.publishAudio(true);
      this.mute = !this.mute;
    },

    openCamera() {
      this.publisher.publishVideo(true);
      this.closecamera = !this.closecamera;
    },

    closeCamera() {
      this.publisher.publishVideo(false);
      this.closecamera = !this.closecamera;
    },
    startChat() {
      this.chatmodal = !this.chatmodal;
    },
    closeChat() {
      this.chatmodal = !this.chatmodal;
    },

    checkParticipants() {
      this.participantsmodal = !this.participantsmodal;
    },

    closeParticipants() {
      this.participantsmodal = !this.participantsmodal;
    },
    // <------------------- 비디오/보이스 On/Off End ------------------->
  },
};
</script>

