const OPENVIDU_SERVER_URL = 'https://' + 'i8a106.p.ssafy.io';
const OPENVIDU_SERVER_SECRET = 'cKlIVFkVgNinXpF';

export default {
  name: 'App',

  components: {
    // <------------------- 화면 출력 Start -------------------->
    UserVideo,
    // <------------------- 화면 출력 End ---------------------->

    // <------------------- 채팅 기능 Start -------------------->
